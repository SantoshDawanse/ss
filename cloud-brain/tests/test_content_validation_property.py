# Custom strategies for generating test data
@st.composite
def valid_content_strategy(draw):
    """Generate valid educational content that should pass validation."""
    subjects = [
        Subject.MATHEMATICS.value,
        Subject.SCIENCE.value,
        Subject.ENGLISH.value,
        Subject.NEPALI.value,
        Subject.SOCIAL_STUDIES.value,
    ]
    
    subject = draw(st.sampled_from(subjects))
    grade = draw(st.integers(min_value=6, max_value=8))
    
    # Generate content appropriate for the subject
    if subject == Subject.MATHEMATICS.value:
        content = draw(st.text(
            alphabet=st.characters(whitelist_categories=('Lu', 'Ll', 'Nd', 'Zs', 'Po')),
            min_size=50,
            max_size=500
        ))
        # Ensure it has mathematical context
        content = f"This lesson teaches addition and subtraction. {content} Students will practice with numbers."
        target_standards = [f"MATH-{grade}-001"]
    elif subject == Subject.SCIENCE.value:
        content = draw(st.text(
            alphabet=st.characters(whitelist_categories=('Lu', 'Ll', 'Nd', 'Zs', 'Po')),
            min_size=50,
            max_size=500
        ))
        content = f"This lesson covers basic science concepts. {content} Students will learn through observation."
        target_standards = [f"SCIENCE-{grade}-001"]
    else:
        content = draw(st.text(
            alphabet=st.characters(whitelist_categories=('Lu', 'Ll', 'Nd', 'Zs', 'Po')),
            min_size=50,
            max_size=500
        ))
        content = f"This lesson introduces language concepts. {content} Students will practice reading."
        target_standards = [f"{subject.upper()}-{grade}-001"]
    
    return ContentValidationRequest(
        content_id=draw(st.text(alphabet=st.characters(whitelist_categories=('Lu', 'Ll', 'Nd')), min_size=5, max_size=20)),
        content_type=draw(st.sampled_from(["lesson", "quiz", "hint"])),
        content=content,
        target_standards=target_standards,
        grade=grade,
        subject=subject,
    )


