@pytest.mark.property_test
@settings(
    max_examples=100,
    deadline=None,
    suppress_health_check=[HealthCheck.function_scoped_fixture, HealthCheck.too_slow]
)
@given(request=valid_content_strategy())
def test_property_1_all_content_must_be_validated(content_validator, request):
    """Property 1: Content Curriculum Alignment
    
    For any content generated by the Cloud Brain, the content must be
    validated by the Curriculum Validator and map to specific Nepal K-12
    curriculum standards.
    
    This property verifies that:
    1. All content goes through validation pipeline
    2. Validation checks curriculum alignment
    3. Content is mapped to curriculum standards
    4. Validation result includes alignment information
    """
    # Validate the content
    result = content_validator.validate_content(request, use_guardrails=False)
    
    # Property 1: Content must be validated
    assert result is not None, "Validation must return a result"
    assert result.content_id == request.content_id, "Result must match request"
    assert result.content_type == request.content_type, "Content type must be preserved"
    
    # Property 1: Validation must check curriculum alignment
    assert (
        ValidationCheckType.CURRICULUM_ALIGNMENT in result.passed_checks
        or ValidationCheckType.CURRICULUM_ALIGNMENT in result.failed_checks
    ), "Curriculum alignment check must be performed"
    
    # Property 1: Result must include alignment score (if alignment check succeeded)
    # Note: alignment_score may be None if MCP server is unavailable or errors occur
    if result.alignment_score is not None:
        assert 0.0 <= result.alignment_score <= 1.0, "Alignment score must be between 0 and 1"
    
    # Property 1: Content must have a definitive validation status
    assert result.status in [
        ValidationStatus.PASSED,
        ValidationStatus.FAILED,
        ValidationStatus.NEEDS_REGENERATION,
    ], "Validation must return a valid status"


@pytest.mark.property_test
@settings(
    max_examples=100,
    deadline=None,
    suppress_health_check=[HealthCheck.function_scoped_fixture, HealthCheck.too_slow]
)
@given(request=valid_content_strategy())
def test_property_1_curriculum_standards_mapping(content_validator, request):
    """Property 1: Content must map to specific curriculum standards
    
    This property verifies that validation checks content against the
    target curriculum standards specified in the request.
    """
    # Validate the content
    result = content_validator.validate_content(request, use_guardrails=False)
    
    # Property 1: Validation must reference target standards
    # The curriculum validator should have checked alignment with target_standards
    assert request.target_standards is not None, "Request must have target standards"
    assert len(request.target_standards) > 0, "Request must specify at least one standard"
    
    # Property 1: If curriculum alignment check was performed, it used the standards
    if ValidationCheckType.CURRICULUM_ALIGNMENT in result.passed_checks:
        # Content passed curriculum alignment, meaning it was checked against standards
        assert result.alignment_score >= content_validator.curriculum_validator.alignment_threshold
    elif ValidationCheckType.CURRICULUM_ALIGNMENT in result.failed_checks:
        # Content failed curriculum alignment, but check was still performed
        alignment_issues = [
            issue for issue in result.issues
            if issue.check_type == ValidationCheckType.CURRICULUM_ALIGNMENT
        ]
        assert len(alignment_issues) > 0, "Failed alignment must have issues documented"


