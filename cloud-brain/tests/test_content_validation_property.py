@pytest.mark.property_test
@settings(
    max_examples=50,
    deadline=None,
    suppress_health_check=[HealthCheck.function_scoped_fixture, HealthCheck.too_slow]
)
@given(
    request=valid_content_strategy(),
    regeneration_attempts=st.integers(min_value=0, max_value=3)
)
def test_property_1_regeneration_workflow(content_validator, request, regeneration_attempts):
    """Property 1: Content regeneration workflow maintains validation integrity
    
    This property verifies that when content fails validation and is
    regenerated, the validation process is repeated and maintains the same
    standards.
    """
    regeneration_count = 0
    
    def mock_regeneration(req, issues):
        nonlocal regeneration_count
        regeneration_count += 1
        
        # Simulate regeneration by returning slightly modified content
        return f"Regenerated content (attempt {regeneration_count}): {req.content}"
    
    # Validate with regeneration
    result, attempts = content_validator.validate_with_regeneration(
        request,
        mock_regeneration,
        use_guardrails=False,
    )
    
    # Property 1: Regeneration must respect max attempts
    assert attempts <= content_validator.max_regeneration_attempts, \
        "Regeneration must not exceed max attempts"
    
    # Property 1: Final result must have validation status
    assert result.status in [
        ValidationStatus.PASSED,
        ValidationStatus.FAILED,
        ValidationStatus.NEEDS_REGENERATION,
    ], "Final result must have valid status"
    
    # Property 1: If passed, all critical checks must pass
    if result.status == ValidationStatus.PASSED:
        critical_issues = [i for i in result.issues if i.severity == "critical"]
        assert len(critical_issues) == 0, \
            "Passed content must not have critical issues"


@pytest.mark.property_test
@settings(
    max_examples=50,
    deadline=None,
    suppress_health_check=[HealthCheck.function_scoped_fixture, HealthCheck.too_slow]
)
@given(
    content_type=st.sampled_from(["lesson", "quiz", "hint"]),
    grade=st.integers(min_value=6, max_value=8),
    subject=st.sampled_from([s.value for s in Subject])
)
def test_property_1_validation_consistency(content_validator, content_type, grade, subject):
    """Property 1: Validation is consistent across content types
    
    This property verifies that validation standards are applied consistently
    regardless of content type (lesson, quiz, hint).
    """
    # Create two similar requests with same content but different IDs
    content = "This is a simple educational lesson about basic concepts. Students will learn fundamental ideas."
    
    request1 = ContentValidationRequest(
        content_id="test-001",
        content_type=content_type,
        content=content,
        target_standards=[f"{subject.upper()}-{grade}-001"],
        grade=grade,
        subject=subject,
    )
    
    request2 = ContentValidationRequest(
        content_id="test-002",
        content_type=content_type,
        content=content,
        target_standards=[f"{subject.upper()}-{grade}-001"],
        grade=grade,
        subject=subject,
    )
    
    # Validate both
    result1 = content_validator.validate_content(request1, use_guardrails=False)
    result2 = content_validator.validate_content(request2, use_guardrails=False)
    
    # Property 1: Same content should produce same validation outcome
    assert result1.status == result2.status, \
        "Same content should produce same validation status"
    
    assert set(result1.passed_checks) == set(result2.passed_checks), \
        "Same content should pass same checks"
    
    assert set(result1.failed_checks) == set(result2.failed_checks), \
        "Same content should fail same checks"
    
    # Alignment scores should be similar (allowing for minor variations)
    if result1.alignment_score is not None and result2.alignment_score is not None:
        assert abs(result1.alignment_score - result2.alignment_score) < 0.1, \
            "Same content should produce similar alignment scores"
