@st.composite
def mixed_content_strategy(draw):
    """Generate content that may be safe or unsafe."""
    content_type = draw(st.sampled_from(["safe", "unsafe", "sensitive"]))
    
    if content_type == "safe":
        return draw(safe_content_strategy())
    elif content_type == "unsafe":
        return draw(unsafe_content_strategy())
    else:
        return draw(culturally_sensitive_content_strategy())


