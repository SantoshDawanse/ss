@pytest.mark.property_test
@settings(
    max_examples=50,
    deadline=None,
    suppress_health_check=[HealthCheck.function_scoped_fixture, HealthCheck.too_slow]
)
@given(
    safe_content=safe_content_strategy(),
    unsafe_content=unsafe_content_strategy()
)
def test_property_16_differential_filtering(safety_filter, safe_content, unsafe_content):
    """Property 16: Safety filter must differentiate between safe and unsafe content
    
    For any pair of safe and unsafe content, the safety filter must correctly
    identify and handle them differently.
    """
    # Filter safe content
    safe_result = safety_filter.filter_content(
        content=safe_content,
        content_id=f"test-diff-safe-{hash(safe_content)}",
        content_type="lesson",
        use_guardrails=False,
    )
    
    # Filter unsafe content
    unsafe_result = safety_filter.filter_content(
        content=unsafe_content,
        content_id=f"test-diff-unsafe-{hash(unsafe_content)}",
        content_type="lesson",
        use_guardrails=False,
    )
    
    # Property 16: Safe content must pass
    assert safe_result.status == ValidationStatus.PASSED, \
        "Safe content must pass filtering"
    
    # Property 16: Unsafe content must be rejected
    assert unsafe_result.status in [ValidationStatus.FAILED, ValidationStatus.NEEDS_REGENERATION], \
        "Unsafe content must be rejected"
    
    # Property 16: Unsafe content must have more issues than safe content
    assert len(unsafe_result.issues) > len(safe_result.issues), \
        "Unsafe content must have more issues than safe content"
    
    # Property 16: Unsafe content must have safety filter failures
    assert ValidationCheckType.SAFETY_FILTER in unsafe_result.failed_checks, \
        "Unsafe content must fail safety filter check"
    
    # Property 16: Safe content must not have safety filter failures
    assert ValidationCheckType.SAFETY_FILTER not in safe_result.failed_checks, \
        "Safe content must not fail safety filter check"


