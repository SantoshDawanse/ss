@pytest.mark.property_test
@settings(
    max_examples=100,
    deadline=None,
    suppress_health_check=[HealthCheck.function_scoped_fixture, HealthCheck.too_slow]
)
@given(content=safe_content_strategy())
def test_property_16_safe_content_passes_filtering(safety_filter, content):
    """Property 16: Safe content must pass safety filtering
    
    For any safe educational content, the safety filter must allow it to pass
    without rejection.
    
    This verifies that the safety filter does not incorrectly reject
    appropriate educational content.
    """
    # Filter the safe content
    result = safety_filter.filter_content(
        content=content,
        content_id=f"test-safe-{hash(content)}",
        content_type="lesson",
        use_guardrails=False,
    )
    
    # Property 16: Safe content must pass
    assert result.status == ValidationStatus.PASSED, \
        f"Safe content should pass filtering but got {result.status}: {result.issues}"
    
    # Property 16: No safety filter failures for safe content
    assert ValidationCheckType.SAFETY_FILTER not in result.failed_checks, \
        "Safe content should not fail safety filter check"
    
    # Property 16: No critical issues for safe content
    critical_issues = [i for i in result.issues if i.severity == "critical"]
    assert len(critical_issues) == 0, \
        f"Safe content should not have critical issues: {critical_issues}"


